package com.api.calendar.math;

public class MathStudy {

	public static void main(String[] args) {
		
		double a = 3456.5324;
		double b = 577.26784;
		double c = 552.2987;
		double d = -845.215;
		
		System.out.println(Math.ceil(a*10) / 10.0);
		System.out.println(Math.round(b*100) / 100.0);
		System.out.println(Math.ceil(c*1000) / 1000.0);
		System.out.println(Math.floor(d*10) / 10.0);
		System.out.println(Math.floor(d*100) / 100.0);
	}

}
